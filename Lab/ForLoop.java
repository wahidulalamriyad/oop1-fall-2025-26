package Lab;

public class ForLoop {
    public static void main(String[] args) {
        // Print numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // Reverse loop from 10 to 1
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
